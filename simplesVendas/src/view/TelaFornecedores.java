    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        Fornecedor fornecedor = new Fornecedor();

        fornecedor.setNome(txtNome.getText());
        fornecedor.setCnpj(fmtCnpj.getText());
        fornecedor.setEmail(txtEmail.getText());
        fornecedor.setTelefone(fmtTelefone.getText());
        fornecedor.setCelular(fmtCelular.getText());
        fornecedor.setCep(fmtCep.getText());
        fornecedor.setEstado(cboUf.getSelectedItem().toString());
        fornecedor.setCidade(txtCidade.getText());
        fornecedor.setBairro(txtBairro.getText());
        fornecedor.setEndereco(txtEndereco.getText());
        try
        {
            fornecedor.setNumero(Integer.parseInt(txtNumero.getText()));
        }
        catch(Exception erro)
        {
            fornecedor.setNumero(0);
        }
        fornecedor.setComplemento(txtComplemento.getText());
        
        FornecedorDAO dao = new FornecedorDAO();

        dao.cadastrarFornecedor(fornecedor);
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        Fornecedor fornecedor = new Fornecedor();
        fornecedor.setId(Integer.parseInt(txtId.getText()));

        FornecedorDAO fornecedorDao = new FornecedorDAO();
        if(fornecedorDao.deletarFornecedor(fornecedor))
        {
            utilitarios.Utilitarios.LimpaTela(tbdPnlDadosBasicos);
            JOptionPane.showMessageDialog(null, "Fornecedor deletado com sucesso ;)");
        }
    }//GEN-LAST:event_btnDeletarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        Fornecedor fornecedor = new Fornecedor();

        try
        {
            fornecedor.setId(Integer.parseInt(txtId.getText()));
        }
        catch(Exception erro)
        {
            fornecedor.setId(0);
        }
        fornecedor.setNome(txtNome.getText());
        fornecedor.setCnpj(fmtCnpj.getText());
        fornecedor.setEmail(txtEmail.getText());
        fornecedor.setTelefone(fmtTelefone.getText());
        fornecedor.setCelular(fmtCelular.getText());
        fornecedor.setCep(fmtCep.getText());
        fornecedor.setEstado(cboUf.getSelectedItem().toString());
        fornecedor.setCidade(txtCidade.getText());
        fornecedor.setBairro(txtBairro.getText());
        fornecedor.setEndereco(txtEndereco.getText());
        try
        {
            fornecedor.setNumero(Integer.parseInt(txtNumero.getText()));
        }
        catch(Exception erro)
        {
            fornecedor.setNumero(0);
        }
        fornecedor.setComplemento(txtComplemento.getText());
        
        FornecedorDAO dao = new FornecedorDAO();

        dao.alterarFornecedor(fornecedor);
        utilitarios.Utilitarios.LimpaTela(tbdPnlDadosBasicos);
    }//GEN-LAST:event_btnEditarActionPerformed

