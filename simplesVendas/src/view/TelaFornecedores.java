    public void listar()
    {
        FornecedorDAO fornecedorDao = new FornecedorDAO();
        List<Fornecedor> fornecedores = fornecedorDao.obterFornecedores();
        
        DefaultTableModel tabelaDados = (DefaultTableModel) tblFornecedores.getModel();
        tabelaDados.setNumRows(0);
        
        for(Fornecedor fornecedor : fornecedores)
        {
            tabelaDados.addRow(
                new Object[]{
                    fornecedor.getId(),
                    fornecedor.getCnpj(),
                    fornecedor.getNome(),
                    fornecedor.getEmail(),
                    fornecedor.getTelefone(),
                    fornecedor.getCelular(),
                    fornecedor.getCep(),
                    fornecedor.getEstado(),
                    fornecedor.getCidade(),
                    fornecedor.getBairro(),
                    fornecedor.getEndereco(),
                    fornecedor.getNumero(),
                    fornecedor.getComplemento()
                }
            );
        }
    }
    
