    public void listar()
    {
        FuncionarioDAO funcionarioDao = new FuncionarioDAO();
        List<Funcionario> funcionarios = funcionarioDao.obterFuncionarios();
        
        DefaultTableModel tabelaDados = (DefaultTableModel) tblFuncionarios.getModel();
        tabelaDados.setNumRows(0);
        
        for(Funcionario funcionario : funcionarios)
        {
            tabelaDados.addRow(
                new Object[]{
                    funcionario.getId(),
                    funcionario.getNome(),
                    funcionario.getRg(),
                    funcionario.getCpf(),
                    funcionario.getEmail(),
                    funcionario.getSenha(),
                    funcionario.getCargo(),
                    funcionario.getNivelAcesso(),
                    funcionario.getTelefone(),
                    funcionario.getCelular(),
                    funcionario.getCep(),
                    funcionario.getEndereco(),
                    funcionario.getNumero(),
                    funcionario.getComplemento(),
                    funcionario.getBairro(),
                    funcionario.getCidade(),
                    funcionario.getUf(),
                }
            );
        }
    }
    
